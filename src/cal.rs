pub fn add(a: i32, b: i32) -> i32 {
    println!("{} + {} = {}", a, b, a + b);
    a + b
}
    
pub fn minus(a: i32, b: i32) -> i32 {
    println!("{} - {} = {}", a, b, a - b);
    a - b
}
    
pub fn multiply(a: i32, b: i32) -> i32 {
    println!("{} * {} = {}", a, b, a * b);
    a * b
}
pub fn division(a: i32, b: i32) -> i32 {
    println!("{} / {} = {}", a, b, a / b);
    a / b
}
pub fn remain(a: i32, b: i32) -> i32 {
    println!("{} % {} = {}", a, b, a % b);
    a % b
}